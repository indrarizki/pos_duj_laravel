    {
        $data = User::wherein('role', ['kasir', 'resepsionist', 'manager', 'manager_center'])
        ->where('id', '!=', Auth()->user()->id)
        ->orderBy('is_active', 'desc')
        ->get();
        $roles = User::getRules();
        return view('manager_center.karyawan', ['data' => $data, 'roles' => $roles]);
    }

